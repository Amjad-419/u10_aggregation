public class Palette
{
   private String ladung;
   private int masse;

   public void setLadung(String bezeichnung)
   {
      this.ladung = bezeichnung;
   }

   public String getLadung()
   {
      return this.ladung;
   }

   public void setMasse(int masse)
   {
      this.masse = masse;
   }

   public int getMasse()
   {
      return this.masse;
   }
}
